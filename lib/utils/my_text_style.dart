
import 'package:flutter/material.dart';

class MyStyle{
  static myTitleTextStyle(Color textColor){
    return TextStyle(
      color: textColor,
      fontSize: 22,
      fontWeight: FontWeight.bold
    );
  }

  static mySubTitleTextStyle(Color textColor){
    return TextStyle(
      color: textColor,
      fontSize: 16,
    );
  }



}